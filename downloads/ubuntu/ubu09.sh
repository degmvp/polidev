# O monitoramento contínuo permite identificar gargalos,
# falhas e problemas de desempenho antes que impactem
# os serviços em produção.
#
# Leia • Entenda • Execute seu código
# ==========================================================

# ==========================================================
# ETAPA 01. Verificar utilização de CPU e memória
# ==========================================================
top

# ==========================================================
# ETAPA 02. Utilizar interface avançada de monitoramento
# ==========================================================
htop

# ==========================================================
# ETAPA 03. Verificar carga média do sistema
# ==========================================================
uptime

# ==========================================================
# ETAPA 04. Verificar utilização de memória
# ==========================================================
free -h

# ==========================================================
# ETAPA 05. Verificar utilização de disco
# ==========================================================
df -h

# ==========================================================
# ETAPA 06. Verificar utilização por diretório
# ==========================================================
du -sh /var/*

# ==========================================================
# ETAPA 07. Verificar processos com maior consumo de CPU
# ==========================================================
ps aux --sort=-%cpu | head

# ==========================================================
# ETAPA 08. Verificar processos com maior consumo de memória
# ==========================================================
ps aux --sort=-%mem | head

# ==========================================================
# ETAPA 09. Verificar estatísticas da CPU
# ==========================================================
mpstat

# ==========================================================
# ETAPA 10. Verificar estatísticas de memória
# ==========================================================
vmstat

# ==========================================================
# ETAPA 11. Verificar estatísticas de I/O
# ==========================================================
iostat

# ==========================================================
# ETAPA 12. Monitorar atividade de disco
# ==========================================================
iotop

# ==========================================================
# ETAPA 13. Verificar conexões de rede
# ==========================================================
ss -tulnp

# ==========================================================
# ETAPA 14. Verificar interfaces de rede
# ==========================================================
ip addr

# ==========================================================
# ETAPA 15. Monitorar tráfego de rede
# ==========================================================
iftop

# ==========================================================
# ETAPA 16. Verificar espaço utilizado em logs
# ==========================================================
du -sh /var/log/*

# ==========================================================
# ETAPA 17. Verificar temperatura do sistema
# ==========================================================
sensors

# ==========================================================
# ETAPA 18. Exibir informações de hardware
# ==========================================================
lscpu
lsblk

# ==========================================================
# ETAPA 19. Verificar tempo de atividade do servidor
# ==========================================================
uptime

# ==========================================================
# ETAPA 20. Gerar visão geral do ambiente
# ==========================================================
top
free -h
df -h
