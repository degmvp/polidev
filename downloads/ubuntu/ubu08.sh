#
# OBJETIVO:
# Este documento apresenta os principais comandos para
# análise de logs e troubleshooting no Ubuntu Linux.
#
# ATENÇÃO:
# A maioria dos problemas em servidores Linux pode ser
# diagnosticada através da análise adequada dos logs.
#
# Leia • Entenda • Execute seu código
# ==========================================================

# ==========================================================
# ETAPA 01. Verificar mensagens recentes do sistema
# ==========================================================
dmesg

# ==========================================================
# ETAPA 02. Exibir últimas linhas do syslog
# ==========================================================
tail /var/log/syslog

# ==========================================================
# ETAPA 03. Monitorar syslog em tempo real
# ==========================================================
tail -f /var/log/syslog

# ==========================================================
# ETAPA 04. Consultar logs do systemd
# ==========================================================
journalctl

# ==========================================================
# ETAPA 05. Consultar logs do boot atual
# ==========================================================
journalctl -b

# ==========================================================
# ETAPA 06. Consultar logs de um serviço
# ==========================================================
journalctl -u ssh

# ==========================================================
# ETAPA 07. Consultar logs em tempo real
# ==========================================================
journalctl -fu ssh

# ==========================================================
# ETAPA 08. Verificar falhas em serviços
# ==========================================================
systemctl --failed

# ==========================================================
# ETAPA 09. Verificar utilização de disco
# ==========================================================
df -h

# ==========================================================
# ETAPA 10. Verificar utilização de diretórios
# ==========================================================
du -sh /var/log/*

# ==========================================================
# ETAPA 11. Verificar memória disponível
# ==========================================================
free -h

# ==========================================================
# ETAPA 12. Verificar processos ativos
# ==========================================================
ps aux

# ==========================================================
# ETAPA 13. Monitorar processos em tempo real
# ==========================================================
top

# ==========================================================
# ETAPA 14. Verificar portas em uso
# ==========================================================
ss -tulnp

# ==========================================================
# ETAPA 15. Verificar conectividade de rede
# ==========================================================
ping google.com

# ==========================================================
# ETAPA 16. Verificar resolução DNS
# ==========================================================
nslookup google.com

# ==========================================================
# ETAPA 17. Verificar rota de rede
# ==========================================================
traceroute google.com

# ==========================================================
# ETAPA 18. Verificar últimos logins
# ==========================================================
last

# ==========================================================
# ETAPA 19. Verificar tentativas de autenticação
# ==========================================================
grep ssh /var/log/auth.log

# ==========================================================
# ETAPA 20. Exibir eventos críticos do sistema
# ==========================================================
journalctl -p err -b
