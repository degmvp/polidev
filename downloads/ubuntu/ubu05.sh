# ==========================================================
# ETAPA 01. Verificar instalação do SSH
# ==========================================================
ssh -V

# ==========================================================
# ETAPA 02. Instalar OpenSSH Server
# ==========================================================
sudo apt install openssh-server -y

# ==========================================================
# ETAPA 03. Verificar status do serviço SSH
# ==========================================================
sudo systemctl status ssh

# ==========================================================
# ETAPA 04. Iniciar serviço SSH
# ==========================================================
sudo systemctl start ssh

# ==========================================================
# ETAPA 05. Habilitar SSH no boot
# ==========================================================
sudo systemctl enable ssh

# ==========================================================
# ETAPA 06. Verificar porta SSH
# ==========================================================
sudo ss -tulnp | grep ssh

# ==========================================================
# ETAPA 07. Identificar endereço IP do servidor
# ==========================================================
ip addr

# ==========================================================
# ETAPA 08. Conectar remotamente via SSH
# ==========================================================
ssh usuario01@192.168.0.100

# ==========================================================
# ETAPA 09. Encerrar sessão SSH
# ==========================================================
exit

# ==========================================================
# ETAPA 10. Copiar arquivo para servidor remoto
# ==========================================================
scp arquivo.txt usuario01@192.168.0.100:/home/usuario01

# ==========================================================
# ETAPA 11. Copiar arquivo do servidor remoto
# ==========================================================
scp usuario01@192.168.0.100:/home/usuario01/arquivo.txt 

# ==========================================================
# ETAPA 12. Gerar chave SSH
# ==========================================================
ssh-keygen -t rsa -b 4096

# ==========================================================
# ETAPA 13. Copiar chave pública para o servidor
# ==========================================================
ssh-copy-id usuario01@192.168.0.100

# ==========================================================
# ETAPA 14. Testar autenticação por chave
# ==========================================================
ssh usuario01@192.168.0.100

# ==========================================================
# ETAPA 15. Reiniciar serviço SSH
# ==========================================================
sudo systemctl restart ssh

# ==========================================================
# ETAPA 16. Verificar logs do SSH
# ==========================================================
sudo journalctl -u ssh

# ==========================================================
# ETAPA 17. Verificar conexões SSH ativas
# ==========================================================
who

# ==========================================================
# ETAPA 18. Exibir últimas conexões
# ==========================================================
last
